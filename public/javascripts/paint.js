function convertSC(logoData) {
  var workString = '';

  for (var i=0; i<logoData.length;i++) {
    if (logoData.charAt(i) == "0") {
      workString += "000"
    } else if (logoData.charAt(i) == "1") {
      workString += "750"
    } else if (logoData.charAt(i) == "2") {
      workString += "700"
    }
  }

  return workString;

}

